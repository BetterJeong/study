package mid;
import java.util.*;

public class Mid1 {
	
	private String username;
	private String msg;
	
	public Mid1(String username, String msg) {
		this.username = username;
		this.msg = msg;
	}
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}

	
}
